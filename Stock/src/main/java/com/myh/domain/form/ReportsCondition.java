package com.myh.domain.form;

public class ReportsCondition {

	private String dateCondition;
	private String seasonYear;
	private String seasonWhich;
	private String yearWhich;
	private String startdate;
	private String enddate;
	
	public String getDateCondition() {
		return dateCondition;
	}
	public void setDateCondition(String dateCondition) {
		this.dateCondition = dateCondition;
	}
	public String getSeasonYear() {
		return seasonYear;
	}
	public void setSeasonYear(String seasonYear) {
		this.seasonYear = seasonYear;
	}
	public String getSeasonWhich() {
		return seasonWhich;
	}
	public void setSeasonWhich(String seasonWhich) {
		this.seasonWhich = seasonWhich;
	}
	public String getYearWhich() {
		return yearWhich;
	}
	public void setYearWhich(String yearWhich) {
		this.yearWhich = yearWhich;
	}
	public String getStartdate() {
		return startdate;
	}
	public void setStartdate(String startdate) {
		this.startdate = startdate;
	}
	public String getEnddate() {
		return enddate;
	}
	public void setEnddate(String enddate) {
		this.enddate = enddate;
	}
	
	
	
}
